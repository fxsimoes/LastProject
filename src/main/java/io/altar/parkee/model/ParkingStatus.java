package io.altar.parkee.model;

public class ParkingStatus {

	private String Name;
	private String Code;

	public String getName() {
		return Name;
	}

	public void setName(String name) {
		Name = name;
	}

	public String getCode() {
		return Code;
	}

	public void setCode(String code) {
		Code = code;
	}

	public ParkingStatus() {}

}
